package com.immobilis.vo;

public class DocumentoContrato {

	int idContrato;
	String urlBanco;
	String urlConservadorBienesRaices;
	String urlNotaria;
	String urlTasacion;
	public int getIdContrato() {
		return idContrato;
	}
	public void setIdContrato(int idContrato) {
		this.idContrato = idContrato;
	}
	public String getUrlBanco() {
		return urlBanco;
	}
	public void setUrlBanco(String urlBanco) {
		this.urlBanco = urlBanco;
	}
	public String getUrlConservadorBienesRaices() {
		return urlConservadorBienesRaices;
	}
	public void setUrlConservadorBienesRaices(String urlConservadorBienesRaices) {
		this.urlConservadorBienesRaices = urlConservadorBienesRaices;
	}
	public String getUrlNotaria() {
		return urlNotaria;
	}
	public void setUrlNotaria(String urlNotaria) {
		this.urlNotaria = urlNotaria;
	}
	public String getUrlTasacion() {
		return urlTasacion;
	}
	public void setUrlTasacion(String urlTasacion) {
		this.urlTasacion = urlTasacion;
	}

}
